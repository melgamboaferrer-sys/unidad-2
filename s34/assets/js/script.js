//METODOS DE ARRAYS

let fruits = ["manzana", "naranja", "pera", "mango"];
console.log(fruits);

console.log("push");
//push(x) -> agrega el elemento al final del array
fruits.push("fresa");
console.log(fruits);

console.log("pop");
//pop()-> elimina el ultimo elemento del array y lo retorna
fruits.pop();
console.log(fruits);